def cal_route(old, M):
    new_route = {}
    for time, point in old.items():
        Z = M[2, 0] * point[0] + M[2, 1] * point[1] + M[2, 2]
        tempX = (M[0, 0] * point[0] + M[0, 1] * point[1] + M[0, 2]) // Z
        tempY = (M[1, 0] * point[0] + M[1, 1] * point[1] + M[1, 2]) // Z
        new_point = [int(tempX), int(tempY)]
        new_route[time] = new_point
    print(new_route)
    return new_route


